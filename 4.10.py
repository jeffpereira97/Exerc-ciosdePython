# Lista com algumas comidas, para ilustrar o exercício.
comidas = ['pizza', 'bolo', 'lasanha', 'sorvete', 'churrasco', 'macarronada', 'strogonoff', 'torta', 'pão']

# Print dos primeiros três itens na lista. Também podendo ser feito com [0:3].
print(comidas[:3])

# Print dos três últimos itens na lista.
print(comidas[-3:])

# Print dos elementos do meio da lista, utilizando uma fatia dela.
print(comidas[3:6])