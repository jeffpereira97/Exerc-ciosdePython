# Lista de lugares
lugares = ['salt lake', 'kyoto', 'londres', 'vancouver', 'nova york']

# Print da lista, sem tratamento nenhum
print(lugares)

# Mostrando a lista em ordem alfabética
print(sorted(lugares))

# Revertendo a lista
lugares.reverse()

# Mostrando a lista reversa
print(lugares)

# Revertendo a lista para a forma original
lugares.reverse()

# Mostrando a lista normal
print(lugares)

# Armazenando a lista em ordem alfabética
lugares.sort()

# Mostrando a lista em ordem alfabética
print(lugares)

# Armazenando a lista em ordem alfabética inversa
lugares.sort(reverse=True)

# Mostrando a lista em ordem alfabética inversa
print(lugares)