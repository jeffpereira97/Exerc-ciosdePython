# Este programa foi criado para treinar a forma de tirar espaços indesejados de uma variável.
nome = '   john      '
print(nome.strip() + '\n' + nome.rstrip() + '\n' + nome.lstrip())

# Este programa foi criado para treinar a forma de deixar a string em maiúscula, minúscula e em título.
name = 'john'
print(name.upper() + '\n' + name.lower() + '\n' + name.title())